                ⑤ 회사는 서비스 이용과 관련하여 회원에게 발생한 손해 가운데 회원의 고의, 과실 및 회원의 귀책 사유로 인한 서비스 이용의 장애 및 손해에 대하여 책임을 지지 않는다.<br><br>
