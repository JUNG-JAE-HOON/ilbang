                ① 회사는 제2조 4항의 서비스를 제공할 수 있으며 그 내용은 다음 각 호와 같다.<br>
                <div style="padding-left: 15px;">
                    - 인재 데이터베이스 검색을 위한 이력서 등록 서비스<br>
                    - 온라인 입사 지원 서비스<br>
                    - 헤드 헌팅/아웃 소싱 서비스<br>
                    - 구인/구직과 관련된 제반 서비스<br>
                    - 기타 회사가 추가 개발하거나 제휴 계약 등을 통해 회원에게 제공하는 일체의 서비스
                </div><br>
                <hr><br>
                <h4><b>제9조 (개인회원 정보, 이력서 노출)</b></h4><br>
                ① 개인 회원의 이력서는 개인이 회원가입 또는 이력서 작성 및 수정시 희망한 형태로 이력서를 노출한다.<br>
                ② 회사는 개인 회원이 이력서의 인재 정보 등록/미등록 지정, 이력서상의 연락처 공개/비공개를 자유롭게 선택할 수 있도록 하여야 한다.<br>
                ③ 회사는 개인 회원이 이력서를 인재 정보에 등록하기를 희망했을 경우 유료 이력서 검색을 신청한 기업 회원이 열람 가능하도록 할 수 있다. 다만, 연락처 각 항목이 비공개로 지정된 경우 해당 항목별 연락처를 노출할 수 없다.<br>
                ④ 회사는 개인 회원이 등록한 이력서를 아이디 휴면 상태 이전까지 노출할 수 있으며, 회사가 지정한 기간이 지난 이력서는 개인 회원 탈퇴시 까지 관리할 수 있다.<br>
                ⑤ 회사는 안정적인 서비스를 제공하기 위해 테스트 및 모니터링, 고객 문의 용도에 한하여 개인 회원의 이력서 정보를 열람할 수 있다.<br><br>
                <hr><br>
                <h4><b>제10조 (제휴를 통한 서비스)</b></h4><br>
                ① 회사는 제휴 관계를 체결한 여타 인터넷 웹 사이트 또는 신문, 잡지 등의 오프라인 매체를 통해 사이트에 등록한 개인 회원의 이력서 정보가 열람될 수 있도록 서비스를 제공할 수 있다. 단, 개인 회원의 경우 제휴 서비스를 통해 노출되는 이력서의 연락처 정보는 제10조의 각 항에 따른다. <br>
                ② 회사는 제휴를 통해 타 사이트 및 매체에 등록될 수 있음을 고지하고 동의를 받아야 하며, 제휴 사이트 전체 목록을 사이트 내에서 상시 열람할 수 있도록 해야 한다.<br><br>
                <hr><br>
                <h4><b>제11조 (서비스의 요금)</b></h4><br>
                ① 개인 회원 가입과 이력서 등록은 무료이다. 다만, 기업 회원 또는 사이트에 방문한 기업체에게 자신의 이력서 정보를 보다 효과적으로 노출시키기 위한 유료 옵션 등 회원 가입 목적 외 기타 서비스를 이용하기 위한 별도의 서비스는 유료로 제공될 수 있다.<br>
                ② 회사는 유료 서비스를 제공할 경우 사이트에 요금에 대해서 공지를 하여야 한다.<br>
                ③ 회사는 유료 서비스 이용 금액이 변경되는 경우 변경된 금액 적용일 최소 7일 전(금액의 변경이 소비자에게 불리한 경우에는 30일 전)부터 웹사이트 초기 화면 공지사항 또는 이메일을 통해 고지한다. 다만, 변경 이전에 적용 또는 계약한 금액은 소급하여 적용하지 아니한다.<br><br>
                <hr><br>
                <h4><b>제12조 (서비스 이용 시간)</b></h4><br>
                ① 회사는 특별한 사유가 없는 한 연중 무휴, 1일 24시간 서비스를 제공한다. 다만, 회사는 서비스의 종류나 성질에 따라 제공하는 서비스 중 일부에 대해서는 별도로 이용 시간을 정할 수 있으며, 이 경우 회사는 그 이용 시간을 사전에 회원에게 공지 또는 통지하여야 한다.<br>
                ② 회사는 자료의 가공과 갱신을 위한 시스템 작업 시간, 장애 해결을 위한 보수 작업 시간, 정기 PM작업, 시스템 교체 작업, 회선 장애 등이 발생한 경우 일시적으로 서비스를 중단할 수 있으며 계획된 작업의 경우 공지란에 서비스 중단 시간과 작업 내용을 알려야 한다.<br><br>
                <hr><br>
                <h4><b>제13조 (서비스 제공의 중지)</b></h4><br>
                ① 회사는 다음 각 호에 해당하는 경우 서비스의 제공을 중지할 수 있다.<br>
                <div style="padding-left: 15px;">
                    - 설비의 보수 등 회사의 필요에 의해 사전에 회원들에게 통지한 경우<br>
                    - 기간 통신 사업자가 전기 통신 서비스 제공을 중지하는 경우<br>
                    - 기타 불가항력적인 사유에 의해 서비스 제공이 객관적으로 불가능한 경우
                </div>
                ② 전 항의 경우, 회사는 기간의 정함이 있는 유료 서비스 이용자들에게는 그 이용 기간을 연장하는 등의 방법으로 손실을 보상하여야 한다.<br><br>
                <hr><br>
                <h4><b>제14조 (정보의 제공 및 광고의 게재)</b></h4><br>
                ① 회사는 회원에게 서비스 이용에 필요가 있다고 인정되거나 서비스 개선 및 회원 대상의 서비스 소개 등의 목적으로 하는 각종 정보를 회원이 수신 동의한 경우, 전자우편, 서신우편, SMS(Short Message Service), MMS(Multimedia Message Service)로 제공할 수 있다.<br>
                ② 회사는 제공하는 서비스와 관련되는 정보 또는 광고를 서비스 화면, 홈페이지 등에 게재할 수 있으며, 회원들에게 전자우편 및 SMS, MMS를 통해 알릴 수 있다.<br>
                ③ 회사는 서비스상에 게재되어 있거나 본 서비스를 통한 광고주의 판촉 활동에 회원이 참여하거나 교신 또는 거래를 함으로써 발생하는 모든 손실과 손해에 대해, 회사의 고의·중과실로 인한 경우가 아닌 한, 책임을 지지 않는다.<br>
                ④ 본 서비스의 회원은 서비스 이용 시 노출되는 광고 게재에 대해 동의 하는 것으로 간주한다.<br><br>
                <hr><br>
                <h4><b>제15조 (자료 내용의 책임과 회사의 정보 수정 권한)</b></h4><br>
                ① 자료 내용은 회원이 등록한 개인 정보 및 이력서와 사이트에 게시한 게시물을 말한다.<br>
                ② 회원은 자료 내용 및 게시물을 사실에 근거하여 성실하게 작성해야 하며, 만일 자료의 내용이 사실이 아니거나 부정확하게 작성되어 발생하는 모든 책임은 회원에게 있다.<br>
                ③ 모든 자료 내용의 관리와 작성은 회원 본인이 하는 것이 원칙이나 사정상 위탁 또는 대행 관리를 하더라도 자료 내용의 책임은 회원에게 있으며 회원은 주기적으로 자신의 자료를 확인하여 항상 정확하게 관리가 되도록 노력해야 한다.<br>
                ④ 회사는 개인 회원이 등록한 자료 내용에 오자, 탈자 또는 사회적 통념에 어긋나는 문구가 있을 경우 이를 언제든지 수정할 수 있다.<br>
                ⑤ 개인 회원이 등록한 자료로 인해 타인(또는 타법인)으로부터 허위 사실 및 명예 훼손 등으로 삭제 요청이 접수된 경우 회사는 개인 회원에게 사전 통지 없이 본 자료를 삭제할 수 있으며 삭제 후 메일 등의 방법으로 통지할 수 있다.<br><br>
                <hr><br>
                <h4><b>제16조 (자료 내용의 활용 및 온라인 입사 지원 정보)</b></h4><br>
                ① 개인 회원이 입력한 정보는 개인을 식별할 수 없는 형태로 제공되어 취업 및 관련 동향의 통계 자료로 활용될 수 있으며 그 자료는 매체를 통해 언론에 배포될 수 있다.<br>
                ② 회사는 '사이트'의 온라인 입사 지원 시스템을 통해 해당 기업에게 제공된 개인 회원의 정보는 해당 기업의 인사 자료이며 이에 대한 관리 권한은 해당 기업의 정책에 의한다.<br><br>
                <hr><br>
                <h4><b>제17조 (회사의 의무)</b></h4><br>
                ① 회사는 본 약관에서 정한 바에 따라 계속적, 안정적으로 서비스를 제공할 수 있도록 최선의 노력을 다해야 한다.<br>
                ② 회사는 서비스와 관련한 회원의 불만 사항이 접수되는 경우 이를 즉시 처리하여야 하며, 즉시 처리가 곤란한 경우에는 그 사유와 처리 일정을 서비스 화면 또는 기타 방법을 통해 동 회원에게 통지하여야 한다.<br>
                ③ 회사는 유료 결제와 관련한 결제 사항 정보를 상법 등 관련 법령의 규정에 의하여 5년 이상 보존한다.<br>
                ④ 천재지변 등 예측하지 못한 일이 발생하거나 시스템의 장애가 발생하여 서비스가 중단될 경우 이에 대한 손해에 대해서는 회사가 책임을 지지 않는다. 다만, 자료의 복구나 정상적인 서비스 지원이 되도록 최선을 다할 의무를 진다.<br>
                ⑤ 회원의 자료를 본 서비스 이외의 목적으로 제3자에게 제공하거나 열람시킬 경우 반드시 회원의 동의를 얻어야 한다.<br>
                ⑥ 회원이 등록한 자료로 인하여 사이트의 원활한 운영에 영향을 미친다고 판단될 시, 등록된 모든 자료를 회원의 사전 동의 없이 삭제할 수 있다.<br><br>
                <hr><br>
                <h4><b>제18조 (회원의 의무)</b></h4><br>
                ① 회원은 관계 법령과 본 약관의 규정 및 기타 회사가 통지하는 사항을 준수하여야 하며, 기타 회사의 업무에 방해되는 행위를 해서는 안 된다.<br>
                ② 회원이 신청한 유료 서비스는 등록 또는 신청과 동시에 회사와 채권, 채무 관계가 발생하며, 회원은 이에 대한 요금을 지정한 기일 내에 납부하여야 한다.<br>
                ③ 회원이 결제 수단으로 신용카드를 사용할 경우 비밀번호 등 정보 유실 방지는 회원 스스로 관리해야 한다. 단, 사이트의 결함에 따른 정보 유실의 발생에 대한 책임은 회원의 의무에 해당하지 아니한다.<br>
                ④ 회원은 서비스를 이용하여 얻은 정보를 회사의 사전 동의 없이 복사, 복제, 번역, 출판, 방송 기타의 방법으로 사용하거나 이를 타인에게 제공할 수 없다.<br>
                ⑤ 회원은 본 서비스를 건전한 구인/구직 이외의 목적으로 사용해서는 안되며 이용 중 다음 각 호의 행위를 해서는 안된다.<br>
                <div style="padding-left: 15px;">
                    - 다른 회원의 아이디를 부정 사용하는 행위<br>
                    - 범죄 행위를 목적으로 하거나 기타 범죄 행위와 관련된 행위<br>
                    - 타인의 명예를 훼손하거나 모욕하는 행위<br>
                    - 타인의 지적 재산권 등의 권리를 침해하는 행위<br>
                    - 해킹 행위 또는 바이러스의 유포 행위<br>
                    - 타인의 의사에 반하여 광고성 정보 등 일정한 내용을 계속적으로 전송하는 행위<br>
                    - 서비스의 안정적인 운영에 지장을 주거나 줄 우려가 있다고 판단되는 행위<br>
                    - 사이트의 정보 및 서비스를 이용한 영리 행위<br>
                    - 그 밖에 선량한 풍속, 기타 사회 질서를 해하거나 관계 법령에 위반하는 행위
                </div><br>
                <hr><br>
                <h4><b>제19조 (회원의 가입 해지/서비스 중지/자료 삭제)</b></h4><br>
                ① 개인 회원이 가입 해지를 하고자 할 때는 고객센터 또는 "개인 회원 탈퇴" 메뉴를 이용해 해지 신청을 해야하고, 회사는 즉시 가입 해지(회원 탈퇴)에 필요한 조치를 취한다.<br>
                ② "회원"이 계약을 해지할 경우, 관련 법 및 개인 정보 처리 방침에 따라 "회사"가 회원 정보를 보유하는 경우를 제외하고는 해지 즉시 "회원"의 모든 데이터는 소멸된다.<br>
                ③ 다음의 사항에 해당하는 경우 회사는 사전 동의없이 가입 해지나 서비스 중지, 이력서 삭제 조치를 취할 수 있다.<br>
                <div style="padding-left: 15px;">
                    - 회원의 의무를 성실하게 이행하지 않았을 때<br>
                    - 규정한 유료 서비스 이용 요금을 납부하지 않았을 때<br>
                    - 본 서비스 목적에 맞지 않는 분야에 정보를 활용하여 사회적 물의가 발생한 때<br>
                    - 회원이 등록한 정보의 내용이 사실과 다르거나 조작되었을 때<br>
                    - 기타 본 서비스 명예를 훼손하였거나 회사가 판단하기에 적합하지 않은 목적으로 회원가입을 하였을 때
                </div>
                ④ 개인 회원이 유료 서비스를 이용하는 중 회사의 책임에 의해 정상적인 서비스가 제공되지 않을 경우 회원은 본 서비스의 해지를 요청할 수 있으며 회사는 해지일까지 이용 일수를 1일 기준 금액으로 계산하여 이용 금액을 공제 후 환급한다.<br>
                ⑤ 회사는 회원 가입이 해지된 경우에 해당 회원의 정보를 임의로 삭제할 수 있다.<br><br>
                <hr><br>
                <h4><b>제20조 (휴면 아이디)</b></h4><br>
                ① 회원이 12개월(365일) 이상 로그인을 하지 않은 경우 해당 회원의 아이디는 휴면 아이디가 되어 회원 로그인을 비롯한 모든 서비스에 대한 이용이 정지되고, 회사는 휴면 아이디의 개인 정보를 다른 아이디와 별도로 관리한다.<br>
                ② 회사는 1항에 의한 서비스 이용 정지 30일 전 이메일을 통하여 서비스 이용 정지에 대하여 안내하고, 서비스 이용 정지가 되는 경우 다시 이메일을 통하여 서비스 이용 정지 사실에 대하여 고지한다.<br>
                ③ 회원은 서비스 이용 정지 이후에 사이트 상에서 직접 본인 확인을 거쳐 휴 면상태 해지 신청을 하는 즉시 다시 정상적으로 서비스를 이용할 수 있다.<br><br>
